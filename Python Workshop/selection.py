# Selection / Conditionals
# If condition is true... do this thing, else .... do this thing

# = means assignment, set the value of this
colour = "Purple"
colour = "blue"

# == means Check, is this thing equal to this other thing
if colour == "Purple":
    print("Colour is purple :) ")
else:
    print("Colour is not purple :( ")
    
